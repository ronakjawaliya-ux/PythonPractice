# sort() method = used with lists

students = ["Roger","Rocks","Kaido","Linlin","Newgate","Shiki"]

students.sort()

for i in students:
    print(i)



# For sort in reverse order:
students = ["Roger","Rocks","Kaido","Linlin","Newgate","Shiki"]

students.sort(reverse=True)

for i in students:
    print(i)